int	ft_str_is_numeric(char *str)
{
	int	dd;
	int	i;

	dd = 0;
	i = 0;
	while (str[i] != '\0')
	{
		if ('0' <= str[i] && str[i] <= '9')
			dd++;
		i++;
	}
	if (i != dd)
		return (0);
	return (1);
}
