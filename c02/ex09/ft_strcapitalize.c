char	*ft_strcapitalize(char *str)
{
	int		i;
	int		n;
	char	c;

	i = 0;
	n = 1;
	while (*(str + i) != '\0')
	{
		c = *(str + i);
		if (n == 1 && c >= 'a' && c <= 'z')
			str[i] -= 32;
		else if (n == 0 && c >= 'A' && c <= 'Z')
			str[i] += 32;
		if (c < '0' || (c > '9' && c < 'A') || (c > 'Z' && c < 'a') || c > 122)
			n = 1;
		else
			n = 0;
		i++;
	}
	return (str);
}
